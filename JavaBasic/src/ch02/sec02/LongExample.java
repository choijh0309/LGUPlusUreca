package ch02.sec02;

public class LongExample {

	public static void main(String[] args) {
		long var1 = 10; // 10은 int의 값  
		long var2 = 20L; // ~~L long 형의 값이다.
//		long var3 = 100_000_000_000; // int value로 인식
		long var3 = 100_000_000_000L;
		long var4 = 100_000_000_000l; // 소문자도 가능, ~~L long 형의 값이다.  
	}

}
